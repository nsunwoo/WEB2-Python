# Number
print(1) # integer(정수)

# String
print('Hello World')

# Boolean
print(True)
print(False)

# Expression
print(1 + 1) # +: 산술 연산자
print('Hello '+'World') # +: 이항 연산자

# Comparison Operator(비교 연산자)
print(1 == 1) # True
print(1 < 2) # True
print(1 > 2) # False

# Membership Operator
print('World' in 'Hello World') # True
print('world' in 'Hello World') # False

